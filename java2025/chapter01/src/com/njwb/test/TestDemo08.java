public class TestDemo08 {
    public static void main(String[] args) {
        int a = 10 ;
        a+=2 ; //a= a+2
        System.out.println(a);
        a*=2 ; //a =a*2;
        System.out.println(a);
        a/=5 ; //a=a/5
        System.out.println(a);
        a-=1 ; //a=a-1
        System.out.println(a);
        a%=2 ; //a=a%2
        System.out.println(a);

    }
}
